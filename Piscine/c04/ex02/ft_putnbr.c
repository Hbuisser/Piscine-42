#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long int a;

	a = nb;
	if (a < 0)
	{
		ft_putchar('-');
		a *= -1;
	}
	if ((a / 10) > 0)
		ft_putnbr(a / 10);
	a = (a % 10) + '0';
	ft_putchar(a);
}
