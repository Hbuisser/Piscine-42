#include <stdio.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int x;
	int y;
	int z;
	int w;

	x = *a;
	y = *b;
	z = x / y;
	*a = z;
	w = x % y;
	*b = w;
}
