#include <stdio.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int x;
	int y;

	i = 0;
	while (i < (size / 2))
	{
		x = *(tab + (size - 1 - i));
		y = *(tab + i);
		*(tab + i) = x;
		*(tab + (size - 1 - i)) = y;
		i++;
	}
}
